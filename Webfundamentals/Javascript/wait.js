function bday() {
    var daysuntilbday = 60;

    while (daysuntilbday >= 35) {
        console.log(daysuntilbday + 'days until my bday..long');
        daysuntilbday--;
    }

    while (daysuntilbday >= 5) {
        console.log(daysuntilbday + 'days until my bday...yay!');
        daysuntilbday--;
    }

    while (daysuntilbday >= 1) {
        console.log(daysuntilbday + 'days until my bday...OFMG!');
        daysuntilbday--;
    }

    if (daysuntilbday == 0) {
        console.log('HAPPY BDAY TO ME!!!!');
    }






}

bday()
